#1.	Se desea calcular la distancia recorrida (m) por un móvil que tiene velocidad constante 100 (m/s) durante un tiempo T 30 (Sg), considerar que es un MRU (Movimiento Rectilíneo Uniforme)
print('Ingrese velocidad : ')
velocidad = int(input())
print('Digitar el Tiempo que tardo en segundos: ')
tiempo = int(input())
distancia = velocidad * tiempo
print(f'la distacia recorridad es {distancia} m/s')